-- Reviews table
CREATE TABLE product_service.reviews (
    id               UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    product_id       UUID NOT NULL REFERENCES product_service.products(id) ON DELETE CASCADE,
    author_name      VARCHAR(255) NOT NULL,
    author_id        UUID,
    title            VARCHAR(500),
    content          TEXT NOT NULL,
    rating_quality   INTEGER NOT NULL CHECK(rating_quality BETWEEN 1 AND 10),
    rating_price     INTEGER NOT NULL CHECK(rating_price BETWEEN 1 AND 10),
    rating_design    INTEGER NOT NULL CHECK(rating_design BETWEEN 1 AND 10),
    rating_features  INTEGER NOT NULL CHECK(rating_features BETWEEN 1 AND 10),
    rating_usability INTEGER NOT NULL CHECK(rating_usability BETWEEN 1 AND 10),
    created_at       TIMESTAMP NOT NULL DEFAULT NOW()
);

CREATE INDEX idx_reviews_product ON product_service.reviews(product_id);
CREATE INDEX idx_reviews_author ON product_service.reviews(author_id);

-- Add rating columns to products
ALTER TABLE product_service.products ADD COLUMN average_rating DOUBLE PRECISION DEFAULT 0;
ALTER TABLE product_service.products ADD COLUMN review_count INTEGER DEFAULT 0;

